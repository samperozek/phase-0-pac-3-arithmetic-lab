function add(a,b){
    let sum;
    return sum = a+b;
}
function subtract(a,b){
    let diff;
    return diff = a-b;
}
function multiply(a,b){
    let total;
    return total = a*b;
}
function divide(a,b){
    let total;
    return total = a/b;
}
function increment(n){
    return n+=1;
}
function decrement(n){
    return n-=1;
}
function makeInt(n){
    return parseInt(n,10);
}
function preserveDecimal(n){
    return parseFloat(n);
}
